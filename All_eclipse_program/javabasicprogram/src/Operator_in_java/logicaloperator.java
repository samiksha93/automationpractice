package Operator_in_java;

public class logicaloperator {

	public static void main(String[] args) {
		boolean  bool1 = true, bool2 = false;	
		System.out.println(bool1&&bool2);
		
		System.out.println(bool1||bool2);

		System.out.println(!(bool1&&bool2));
		
		System.out.println(!(bool1||bool2));
	}
	

}